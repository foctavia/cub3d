#include "cub3d.h"

int	is_space(char c)
{
	if ((c > 7 && c < 14) || c == ' ')
		return (TRUE);
	return (FALSE);
}

int	is_digit(char c)
{
	if (c >= '0' && c <= '9')
		return (TRUE);
	return (FALSE);
}
